    ProPilot is a new virtual flight methodology that attempts to simulate real world flight operations in a more
    realistic way. In propilot, the fleet aircraft themselves are modelled, in addition to your performance being
    critiqued - stick to company procedures!

    <br/><br/>


    We implement real life Airline procedures in as realistic a way we can. With the new Dynamic Fleet Timetable, you
    are more restricted than in the casual flights at Euroharmony. It's not possible to fly any flight that you want;
    you can only make flights that have aircraft waiting at the departure airport! When a flight is completed, the
    aircraft moves from the departure airport to the destination airport, delivering a far more realistic feel. Crash
    the airrcaft and the airline will know it! That aircraft becomes unusable until repairs are completed or a
    replacement has been arranged. Furthermore, the Flight Logger software will critique your flight and reward or
    penalise you for your flying accordingly, the record of which appears in your flight log.


    <br/><br/>

    We run propilot events from time to time which are a propilot equivalent of a tour. The event will use a specific
    aircraft type, of which you will have your own individual aircraft. The event will follow a number of legs which
    will be spaced apart with each leg flyable inside a few week window. The event normally spans several months to
    complete and will provide the successful pilot with an award for completion. In some cases special aircraft types
    are used that are not available within the Euroharmony fleet, or are an introduction to a new fleet addition, other
    times the event will be linked with a historical flight or a modern day tie-in such as aid relief or Formula 1
    logistics.

