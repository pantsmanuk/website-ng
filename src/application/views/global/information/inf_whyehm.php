<h2>What type of virtual pilot are you? How do you prefer to fly? What software do you prefer to use?</h2>
<div class="container">
These are all questions that place you, the pilot, at the centre of decision making at Euroharmony. We're not just an online flight log, we're a community of virtual pilots and we try to provide added enjoyment for a wide range of people who each enjoy something different about their flight simulation hobby.
<br /><br />
<center><h3>We believe it's all about choice.</h3></center>
<br />
Euroharmony is positioned to take advantage of professional virtual airline operations for those that wish to fly in this manner, without imposing restrictions and rules on pilots who prefer to enjoy virtual flying in a more relaxed environment.
</div>

<h3>Professional flight simulation</h3>
<div class="container">
<ul>
<li>A timetable that mimics possible real word operations</li>
<li>Propilot - a flight logging and critique tool to monitor correct aircraft operations, fly actual aircraft tail numbers around the network and have them remain there until flown again.</li>
<li>Flight assignments - if you don't wish to choose your flights, the airline can assign them to you</li>
<li>Partnership and involvement with both the IVAO.aero and VATSIM online networks</li></ul>
</div>

<h3>Casual Flight Simulation</h3>
<div class="container">
<ul>
<li>Low hour requirements for promotion within the airline.</li>
<li>Flight routing, providing multi-step routes from hub to hub, generated on the fly.</li>
<li>Flight Charters, opening the world for general aviation, bush and business flights.</li>
<li>Relaxed flying requirements to remain active compared to other virtual airlines.</li></ul>
</div>

<h3>Ready to learn</h3>
<div class="container">
<ul>
<li>Propilot - use it to critique your flying to help you improve.</li>
<li>Training academy - read up on the information to improve your skills.</li>
<li>Instructors - schedule time with a VA instructor to assist you passing a hurdle such as making the move to online flights.</li></ul>
</div>

<h3>For all</h3>
<div class="container">
<ul>
<li>A number of divisions, covering a range of distinct flight preferences from general aviation through bush flying to our tube liner services.</li>
<li>Global hubs, with a focus on Europe.</li>
<li>Tours with awards for successful completion, listed in your flight record.</li>
<li>Events on propilot where you are assigned your own aircraft (careful, don't crash it) for the duration of the multi-leg event (span several months with each leg flyable over a few weeks).</li>
<li>A varied fleet, offering aircraft not used by other VAs</li>
<li>Support for a variety of flight simulators - while you can fly with any general aviation simulator you prefer, currently 'official support' is provided for MSFS 2004, FSX and X-plane.</li></ul>
</div>