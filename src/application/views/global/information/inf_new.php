<h3>What is a Virtual Airline (VA)?</h3>
<div class="container">
    At it's simplest, a Virtual Airline is a community of hobby enthusiasts who enjoy sharing their passion of simulated
    flight with other likeminded individuals.
    <br/><br/>
    A virtual Airline shares similarities with a real one - and may attempt to mimic the operations of real Airlines to
    a greater or lesser extent, while you, the pilot, fly the Airline's aircraft on their routes. A VA will normally
    have a livery that their fleet is operated in that you can download and fly with, a timetable of flights and a
    method of reporting your flights to have a record of your flight hours. More structured VAs will also have ranks,
    awards and software to enhance your simming.
    <br/><br/>
    There are a wide variety of Virtual Airlines, all different, and catering to slightly different tastes. Some are
    ultra realistic - checkrides and exams are mandatory, there are high activity requirements and all operations
    exactly mirror those of a real airline. At the other end of the spectrum, there are flying clubs which provide a
    method of recording flight time, but have no other structure in place at all. There are no proscribed routes or
    aircraft, no activity requirements and sometimes no livery.
    <br/><br/>
    Euroharmony is positioned inbetween these extremes, leaning more towards the structured end. Please see the <a
            href="<?php echo $base_url; ?>information/index/whyehm/">Why Euroharmony?</a> page for more information on
    what we can offer you.
</div>


<h3>What do I need to be able to fly for a Virtual Airline?</h3>
<div class="container">
    You will need a flight simulator and a computer powerful enough to run it. You will also need an internet connection
    to connect to the community you choose. At Euroharmony we currently officially support Microsoft Flight Simulator
    and X-plane, but you can also fly another simulator of your choice as long as you can locate appropriate aircraft
    for it.
    <br/><br/>
    Beyond that, your hobby can be as cheap or as expensive as you wish to make it. There are hardware addons from
    flight yokes, sticks and pedals all the way up to home built cockpits or even full motion simulators. The leading
    flight simulators also have extensive add-on markets where you can purchase aircraft, scenery and other untilities
    to enhance the experience. At a minimum, you should invest in a joystick or yoke, and then a set of pedals. These
    have the most impact for a small(ish) outlay to get you started.
</div>


<h3>What are the benefits of joining a Virtual Airline?</h3>
<div class="container">
    <ul>
        <li>Community - join a group of people who enjoy what you do and who can offer advice, assistance and training
